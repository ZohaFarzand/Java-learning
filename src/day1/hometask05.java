package day1;

public class hometask05 {
    public static void main(String[] args) {
        // Fixed numbers
        int n1 = 2;
        int n2 = 4;
        int n3 = 6;
        int n4 = 10;

        // Calculations
        int sum = n1 + n2 + n3 + n4;
        double average = sum / 4.0;   // average in decimal
        int product = n1 * n2 * n3 * n4;

        // Display results
        System.out.println("Numbers: " + n1 + ", " + n2 + ", " + n3 + ", " + n4);
        System.out.println("Sum = " + sum);
        System.out.println("Average = " + average);
        System.out.println("Product = " + product);
    }
}
